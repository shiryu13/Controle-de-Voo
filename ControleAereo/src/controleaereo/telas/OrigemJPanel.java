    private void carregarjTable(){
         List<Origem> lista = dao.listar();
        DefaultTableModel dfm = (DefaultTableModel) jTable.getModel();
        int linhas = dfm.getRowCount();
        for(int i = 0; i < linhas; i++){
            dfm.removeRow(0);
        }
        for(Origem origem : lista){
            Object[] linha = new Object[4];
            linha[0] = origem.getId();
            linha[1] = origem.getCidade();
            linha[2] = origem.getPais();
            linha[3] = origem.getLatLong();
            
            dfm.addRow(linha);
        }
    }
    
    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        // TODO add your handling code here:
        Origem o = new Origem();
        int id = Integer.parseInt(jTextFieldId.getText());
        o.setId(id);
        o.setCidade(jTextFieldCidade.getText());
        o.setPais(jTextFieldPais.getText());
        o.setLatLong(jTextFieldLatLong.getText());
        
        if(dao.getById(id) == null){
            dao.inserir(o);
        }else{
            dao.editar(o);
        }
        jButtonLimparActionPerformed(evt);
        carregarjTable();
    }//GEN-LAST:event_jButtonSalvarActionPerformed

