    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        // TODO add your handling code here:
        int linha = jTable.getSelectedRow();
        if(linha >= 0){
            int id = (int) jTable.getValueAt(linha, 0);
            Origem o = dao.getById(id);
            jTextFieldId.setText(o.getId()+"");
            jTextFieldCidade.setText(o.getCidade()+"");
            jTextFieldPais.setText(o.getPais()+"");
            jTextFieldLatLong.setText(o.getLatLong()+"");
        }
    }//GEN-LAST:event_jButtonEditarActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        // TODO add your handling code here:
        int[] linhas = jTable.getSelectedRows();
        for(int linha : linhas){
            int id = (int) jTable.getValueAt(linha, 0);
            dao.remover(id);
        }
        carregarjTable();
    }//GEN-LAST:event_jButtonExcluirActionPerformed


