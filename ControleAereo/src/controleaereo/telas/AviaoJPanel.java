    private void carregarjTable(){
        List<Aviao> lista = dao.listar();
        DefaultTableModel dfm = (DefaultTableModel) jTable.getModel();
        int linhas = dfm.getRowCount();
        for(int i = 0; i < linhas; i++){
            dfm.removeRow(0);
        }
        for(Aviao aviao : lista){
            Object[] linha = new Object[4];
            linha[0] = aviao.getId();
            linha[1] = aviao.getFabricante();
            linha[2] = aviao.getModelo();
            linha[3] = aviao.getLatLong();
            
            dfm.addRow(linha);
        }
    }

