    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        // TODO add your handling code here:
        Aviao a = new Aviao();
        //Origem o = new Origem();
        //Destino dest = new Destino();
        int id = Integer.parseInt(jTextFieldId.getText());
        a.setId(id);
        a.setFabricante(jTextFieldFabricante.getText());
        a.setModelo(jTextFieldModelo.getText());
        a.setLatLong(jTextFieldLatLong.getText());
        //o.setOrigem(jTextFieldOrigem.getText());
        //a.setOrigem(o);
        //dest.setDestino(jTextFieldDestino.getText());
        //a.setDestino(dest);
        
  
        if(dao.getById(id) == null){
            dao.inserir(a);
        }else{
            dao.editar(a);
        }
        jButtonLimparActionPerformed(evt);
        carregarjTable();
    }//GEN-LAST:event_jButtonSalvarActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        // TODO add your handling code here:
        int[] linhas = jTable.getSelectedRows();
        for(int linha : linhas){
            int id = (int) jTable.getValueAt(linha, 0);
            dao.remover(id);
        }
        carregarjTable();
    }//GEN-LAST:event_jButtonExcluirActionPerformed

    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        // TODO add your handling code here:
        int linha = jTable.getSelectedRow();
        if(linha >= 0){
            int id = (int) jTable.getValueAt(linha, 0);
            Aviao a = dao.getById(id);
            jTextFieldId.setText(a.getId()+"");
            jTextFieldFabricante.setText(a.getFabricante()+"");
            jTextFieldModelo.setText(a.getModelo()+"");
            jTextFieldLatLong.setText(a.getLatLong()+"");
        }
    }//GEN-LAST:event_jButtonEditarActionPerformed


